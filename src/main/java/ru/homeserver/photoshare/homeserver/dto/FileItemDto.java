package ru.homeserver.photoshare.homeserver.dto;

/*
 * DTO = Data Transfer Object
 *
 * Это объект, который мы используем для передачи данных
 * между backend и frontend.
 *
 * Почему не отдаем Path или File напрямую?
 * - это неудобно
 * - в них слишком много лишнего
 * - их сложнее и опаснее сериализовать в JSON
 *
 * Поэтому мы формируем специальный "плоский" объект,
 * в котором только те данные, которые реально нужны странице.
 *
 * record в Java — это краткая форма immutable-класса.
 * Он автоматически создает:
 * - конструктор
 * - getters-подобные методы (name(), size() и т.д.)
 * - equals/hashCode
 * - toString
 */
public record FileItemDto(
        // Имя файла или папки, которое увидит пользователь
        String name,

        // Относительный путь внутри rootPath.
        // Например:
        // "photos/2026/pic1.jpg"
        //
        // Мы храним именно относительный путь,
        // а не абсолютный, чтобы:
        // - не светить пути системы
        // - удобнее передавать его в API
        String relativePath,

        // Признак: папка это или нет
        boolean directory,

        // Размер файла в байтах.
        // Для папок обычно ставим 0.
        long size,

        // Логический тип, который мы сами определяем:
        // "directory", "image", "video", "file"
        //
        // Он нужен фронтенду, чтобы понять:
        // - какую иконку показать
        // - что делать по клику
        // - нужно ли рисовать thumbnail
        String type,

        // URL, по которому можно открыть файл напрямую.
        // Для картинки это будет URL изображения.
        // Для видео — URL видео.
        String previewUrl,

        // URL миниатюры.
        // - у изображений часто совпадает с previewUrl
        // - у видео это отдельный jpg
        String thumbnailUrl,
        // URL для скачивания файла
        String downloadUrl,
        // Дата изменения файла/папки в миллисекундах
        long lastModified,
        // Дата создания из метаданных, если есть.
        // Если нет — fallback на дату изменения.
        long createdAt
) {
}
